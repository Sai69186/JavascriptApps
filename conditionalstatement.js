let average = 82;
if (average >= 90) {
  console.log("Grade is:A");
} else if (average < 90 && average >= 80) {
  console.log("grade:B");
} else if (average < 80 && average >= 70) {
  console.log("Grade:C");
} else if (average < 70 && average >= 60) {
  console.log("Grade:D");
} else {
  console.log("NO GRADE");
}
